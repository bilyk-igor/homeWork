package core_03;

public class Human {

	private String name;
	private int age;
	private String job;
	private int payment;
	
	public Human(String name, int age, String job, int payment) {
		super();
		this.name = name;
		this.age = age;
		this.job = job;
		this.payment = payment;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getJob() {
		return job;
	}

	public void setJob(String job) {
		this.job = job;
	}

	public int getPayment() {
		return payment;
	}

	public void setPayment(int payment) {
		this.payment = payment;
	}
	
	
	
	
	
	
}
